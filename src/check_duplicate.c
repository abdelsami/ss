#include "push_swap.h"

int	check_duplicate(int *ar, int ac)
{
	int	i;
	int	j;

	i = 0;
	while (i < ac - 1)
	{
		j = i + 1;
		while (j < ac - 1)
		{
			if (ar[i] == ar[j])
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
